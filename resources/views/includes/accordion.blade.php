        <h5>
          <p>
            En la actualidad, la DGPJ emite dos tipos de certificados a las asociaciones civiles: a) De Vigencia y Regularidad, y b) De Vigencia.
          </p>
          <p>
            <strong>¿QUÉ SON Y PARA QUÉ SIRVEN?</strong>
          </p>
          <p>
            El certificado de “Vigencia” acredita que la asociación que lo solicita se encuentra registrada ante la DGPJ. Por su parte, el de “Vigencia y Regularidad”, además de la inscripción, acredita que la asociación mantiene regularidad en la presentación de sus obligaciones formales frente a la DGPJ. Este último resulta de gran importancia para la vida de las asociaciones, ya que es pedido por organismos gubernamentales y privados para otorgar exenciones fiscales, subsidios, para la apertura de cuentas bancarias, etc.
          </p>
          <p>
            <strong>¿CÓMO LO PIDO?</strong>
          </p>
          <p>
            • Minuta rogatoria Anexo 2 Descargar aquí, con firma certificada de presidente o secretario, tildando trámite “Certificado de Vigencia”.
          </p>
          <p>
            • Declaración Jurada declarando ser presidente o secretario de la entidad solicitante Descargar modelo.
          </p>
          <p>
            Nota. La emisión de uno u otro certificado dependerá de la situación en que se encuentre la asociación peticionante respecto al cumplimiento de sus obligaciones formales frente a la DGPJ. Las entidades que se encuentren al día, están eximidas de presentar la declaración jurada. En caso de duda del estado de situación, aconsejamos su acompañamiento.
          </p>
